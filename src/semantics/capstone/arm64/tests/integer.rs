use super::common::assert_complete_semantics;

#[test]
fn integer_semantics_regressions_stay_complete() {
    let cases = [
        ("adrp x0, #0", vec![0x00, 0x00, 0x00, 0x90]),
        ("sxtw x0, w1", vec![0x20, 0x7c, 0x40, 0x93]),
        ("sxtb x0, w1", vec![0x20, 0x1c, 0x40, 0x93]),
        ("sxth x0, w1", vec![0x20, 0x3c, 0x40, 0x93]),
        ("asr x0, x1, #3", vec![0x20, 0xfc, 0x43, 0x93]),
        ("ror x0, x1, #8", vec![0x20, 0x20, 0xc1, 0x93]),
        ("lsl x0, x1, #3", vec![0x20, 0xf0, 0x7d, 0xd3]),
        ("lsr x0, x1, #3", vec![0x20, 0xfc, 0x43, 0xd3]),
        ("ubfx x0, x1, #4, #8", vec![0x20, 0x2c, 0x44, 0xd3]),
        ("sbfx x0, x1, #4, #8", vec![0x20, 0x2c, 0x44, 0x93]),
        ("ubfiz x0, x1, #4, #8", vec![0x20, 0x1c, 0x7c, 0xd3]),
        ("bfxil x0, x1, #4, #8", vec![0x20, 0x2c, 0x44, 0xb3]),
        ("bfi x0, x1, #4, #8", vec![0x20, 0x1c, 0x7c, 0xb3]),
        ("sbfiz x0, x1, #4, #8", vec![0x20, 0x1c, 0x7c, 0x93]),
        ("bics x0, x1, x2", vec![0x20, 0x00, 0x22, 0xea]),
        ("bic x0, x1, x2", vec![0x20, 0x00, 0x22, 0x8a]),
        ("neg x0, x1", vec![0xe0, 0x03, 0x01, 0xcb]),
        ("movk x0, #0x1234, lsl #16", vec![0x80, 0x46, 0xa2, 0xf2]),
        ("adc x0, x1, x2", vec![0x20, 0x00, 0x02, 0x9a]),
        ("sbc x0, x1, x2", vec![0x20, 0x00, 0x02, 0xda]),
        ("clz x0, x1", vec![0x20, 0x10, 0xc0, 0xda]),
        ("eon x0, x1, x2", vec![0x20, 0x00, 0x22, 0xca]),
        ("movz x0, #0x1234, lsl #16", vec![0x80, 0x46, 0xa2, 0xd2]),
        ("movn x0, #0x1234, lsl #16", vec![0x80, 0x46, 0xa2, 0x92]),
        ("rbit x0, x1", vec![0x20, 0x00, 0xc0, 0xda]),
        ("rev x0, x1", vec![0x20, 0x0c, 0xc0, 0xda]),
        ("rev16 x0, x1", vec![0x20, 0x04, 0xc0, 0xda]),
        ("rev32 x0, x1", vec![0x20, 0x08, 0xc0, 0xda]),
    ];

    for (name, bytes) in cases {
        assert_complete_semantics(name, &bytes);
    }
}
