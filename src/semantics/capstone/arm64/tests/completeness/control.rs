use super::super::support::assert_complete_semantics;

#[test]
fn control_semantics_regressions_stay_complete() {
    let cases = [
        ("csel x0, x1, x2, eq", vec![0x20, 0x00, 0x82, 0x9a]),
        ("cset x0, eq", vec![0xe0, 0x17, 0x9f, 0x9a]),
        ("csetm x0, eq", vec![0xe0, 0x13, 0x9f, 0xda]),
        ("csinc x0, x1, x2, eq", vec![0x20, 0x04, 0x82, 0x9a]),
        ("csinv x0, x1, x2, eq", vec![0x20, 0x00, 0x82, 0xda]),
        ("csneg x0, x1, x2, eq", vec![0x20, 0x04, 0x82, 0xda]),
        ("cneg x0, x1, eq", vec![0x20, 0x14, 0x81, 0xda]),
        ("cinc x0, x1, eq", vec![0x20, 0x14, 0x81, 0x9a]),
        ("fcsel d0, d1, d2, eq", vec![0x20, 0x0c, 0x62, 0x1e]),
        ("cmn x0, x1", vec![0x1f, 0x00, 0x01, 0xab]),
        ("ccmp x0, x1, #0, eq", vec![0x00, 0x00, 0x41, 0xfa]),
        ("ccmn x0, x1, #0, eq", vec![0x00, 0x00, 0x41, 0xba]),
        ("tbz w0, #3, #0x10", vec![0x80, 0x00, 0x18, 0x36]),
        ("svc #0", vec![0x01, 0x00, 0x00, 0xd4]),
    ];

    for (name, bytes) in cases {
        assert_complete_semantics(name, &bytes);
    }
}
