use super::super::support::assert_complete_semantics;

#[test]
fn atomic_semantics_regressions_stay_complete() {
    let cases = [
        ("stlrh w0, [x1]", vec![0x20, 0xfc, 0x9f, 0x48]),
        ("ldar x0, [x1]", vec![0x20, 0xfc, 0xdf, 0xc8]),
        ("ldarb w0, [x1]", vec![0x20, 0xfc, 0xdf, 0x08]),
        ("ldarh w0, [x1]", vec![0x20, 0xfc, 0xdf, 0x48]),
        ("ldaxr x0, [x1]", vec![0x20, 0xfc, 0x5f, 0xc8]),
        ("ldaxrb w0, [x1]", vec![0x20, 0xfc, 0x5f, 0x08]),
        ("ldaxrh w0, [x1]", vec![0x20, 0xfc, 0x5f, 0x48]),
        ("ldxp x0, x1, [x2]", vec![0x40, 0x04, 0x7f, 0xc8]),
        ("ldxr x0, [x1]", vec![0x20, 0x7c, 0x5f, 0xc8]),
        ("ldxrb w0, [x1]", vec![0x20, 0x7c, 0x5f, 0x08]),
        ("ldxrh w0, [x1]", vec![0x20, 0x7c, 0x5f, 0x48]),
        ("stlxr w0, x1, [x2]", vec![0x41, 0xfc, 0x00, 0xc8]),
        ("stlxrb w0, w1, [x2]", vec![0x41, 0xfc, 0x00, 0x08]),
        ("stlxrh w0, w1, [x2]", vec![0x41, 0xfc, 0x00, 0x48]),
        ("stxp w0, x1, x2, [x3]", vec![0x61, 0x08, 0x20, 0xc8]),
        ("stxr w0, x1, [x2]", vec![0x41, 0x7c, 0x00, 0xc8]),
        ("stxrb w0, w1, [x2]", vec![0x41, 0x7c, 0x00, 0x08]),
        ("stxrh w0, w1, [x2]", vec![0x41, 0x7c, 0x00, 0x48]),
    ];

    for (name, bytes) in cases {
        assert_complete_semantics(name, &bytes);
    }
}
