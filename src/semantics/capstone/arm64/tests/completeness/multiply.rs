use super::super::support::assert_complete_semantics;

#[test]
fn multiply_semantics_regressions_stay_complete() {
    let cases = [
        ("sdiv x0, x1, x2", vec![0x20, 0x0c, 0xc2, 0x9a]),
        ("udiv x0, x1, x2", vec![0x20, 0x08, 0xc2, 0x9a]),
        ("mul x0, x1, x2", vec![0x20, 0x7c, 0x02, 0x9b]),
        ("umulh x0, x1, x2", vec![0x20, 0x7c, 0xc2, 0x9b]),
        ("msub x0, x1, x2, x3", vec![0x20, 0x8c, 0x02, 0x9b]),
        ("madd x0, x1, x2, x3", vec![0x20, 0x0c, 0x02, 0x9b]),
        ("umull x0, w1, w2", vec![0x20, 0x7c, 0xa2, 0x9b]),
        ("umaddl x0, w1, w2, x3", vec![0x20, 0x0c, 0xa2, 0x9b]),
        ("smull x0, w1, w2", vec![0x20, 0x7c, 0x22, 0x9b]),
        ("smaddl x0, w1, w2, x3", vec![0x20, 0x0c, 0x22, 0x9b]),
        ("mneg x0, x1, x2", vec![0x20, 0xfc, 0x02, 0x9b]),
        ("smsubl x0, w1, w2, x3", vec![0x20, 0x8c, 0x22, 0x9b]),
        ("smulh x0, x1, x2", vec![0x20, 0x7c, 0x42, 0x9b]),
        ("umnegl x0, w1, w2", vec![0x20, 0xfc, 0xa2, 0x9b]),
        ("umsubl x0, w1, w2, x3", vec![0x20, 0x8c, 0xa2, 0x9b]),
    ];

    for (name, bytes) in cases {
        assert_complete_semantics(name, &bytes);
    }
}
