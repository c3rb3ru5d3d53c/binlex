use super::super::support::assert_complete_semantics;

#[test]
fn vector_semantics_regressions_stay_complete() {
    let cases = [
        ("fmov d0, d1", vec![0x20, 0x40, 0x60, 0x1e]),
        ("fmov d1, x0", vec![0x01, 0x00, 0x67, 0x9e]),
        ("fmov s0, w1", vec![0x20, 0x00, 0x27, 0x1e]),
        ("fmov d0, #1.0", vec![0x00, 0x10, 0x6e, 0x1e]),
        ("fmov s0, #1.0", vec![0x00, 0x10, 0x2e, 0x1e]),
        ("fmov x0, d1", vec![0x20, 0x00, 0x66, 0x9e]),
        ("fmov x1, d0", vec![0x01, 0x00, 0x66, 0x9e]),
        ("fmov w0, s1", vec![0x20, 0x00, 0x26, 0x1e]),
        ("fabs d0, d1", vec![0x20, 0xc0, 0x60, 0x1e]),
        ("fneg d0, d1", vec![0x20, 0x40, 0x61, 0x1e]),
        ("fcmp d0, d1", vec![0x00, 0x20, 0x61, 0x1e]),
        ("fcmp s0, s1", vec![0x00, 0x20, 0x21, 0x1e]),
        ("fcmp d0, #0.0", vec![0x08, 0x20, 0x60, 0x1e]),
        ("fcmp s0, #0.0", vec![0x08, 0x20, 0x20, 0x1e]),
        ("fcmpe s0, s1", vec![0x10, 0x20, 0x21, 0x1e]),
        ("fcmpe d0, #0.0", vec![0x18, 0x20, 0x60, 0x1e]),
        ("fcmpe s0, #0.0", vec![0x18, 0x20, 0x20, 0x1e]),
        ("fccmp d0, d1, #0, eq", vec![0x00, 0x04, 0x61, 0x1e]),
        ("fadd d0, d1, d2", vec![0x20, 0x28, 0x62, 0x1e]),
        ("fmadd d0, d1, d2, d3", vec![0x20, 0x0c, 0x42, 0x1f]),
        ("fmsub d0, d1, d2, d3", vec![0x20, 0x8c, 0x42, 0x1f]),
        ("fsub d0, d1, d2", vec![0x20, 0x38, 0x62, 0x1e]),
        ("fdiv d0, d1, d2", vec![0x20, 0x18, 0x62, 0x1e]),
        ("fmin d0, d1, d2", vec![0x20, 0x58, 0x62, 0x1e]),
        ("fmax d0, d1, d2", vec![0x20, 0x48, 0x62, 0x1e]),
        ("scvtf d0, x1", vec![0x20, 0x00, 0x62, 0x9e]),
        ("ucvtf d0, x1", vec![0x20, 0x00, 0x63, 0x9e]),
        ("fcvtzs x0, d1", vec![0x20, 0x00, 0x78, 0x9e]),
        ("fcvtzu x0, d1", vec![0x20, 0x00, 0x79, 0x9e]),
        ("cmeq v0.16b, v1.16b, v2.16b", vec![0x20, 0x8c, 0x22, 0x6e]),
        ("cmeq v0.2s, v1.2s, v2.2s", vec![0x20, 0x8c, 0xa2, 0x2e]),
        ("cmhi v0.16b, v1.16b, v2.16b", vec![0x20, 0x34, 0x22, 0x6e]),
        ("cmhi v0.2s, v1.2s, v2.2s", vec![0x20, 0x34, 0xa2, 0x2e]),
        ("dup v0.16b, w1", vec![0x20, 0x0c, 0x01, 0x4e]),
        ("dup v0.2d, x1", vec![0x20, 0x0c, 0x08, 0x4e]),
        ("cnt v0.16b, v1.16b", vec![0x20, 0x58, 0x20, 0x4e]),
        ("cnt v0.8b, v1.8b", vec![0x20, 0x58, 0x20, 0x0e]),
        ("addv b0, v1.16b", vec![0x20, 0xb8, 0x31, 0x4e]),
        ("addv s0, v1.4s", vec![0x20, 0xb8, 0xb1, 0x4e]),
        ("ld1 { v0.16b }, [x1]", vec![0x20, 0x70, 0x40, 0x4c]),
        ("ld1 {v0.d}[1], [x1]", vec![0x20, 0x84, 0x40, 0x4d]),
        ("ld1 {v1.s}[1], [x11]", vec![0x61, 0x91, 0x40, 0x0d]),
        ("sshll v0.8h, v1.8b, #0", vec![0x20, 0xa4, 0x08, 0x0f]),
        ("sshll v0.4s, v1.4h, #0", vec![0x20, 0xa4, 0x10, 0x0f]),
        ("sshll v1.4s, v0.4h, #0", vec![0x01, 0xa4, 0x10, 0x0f]),
        ("sshll v0.2d, v1.2s, #0", vec![0x20, 0xa4, 0x20, 0x0f]),
        ("uaddlv h0, v1.16b", vec![0x20, 0x38, 0x30, 0x6e]),
        ("uaddlv h0, v1.8b", vec![0x20, 0x38, 0x30, 0x2e]),
        ("uzp1 v0.16b, v1.16b, v2.16b", vec![0x20, 0x18, 0x02, 0x4e]),
        ("uzp1 v0.4s, v1.4s, v2.4s", vec![0x20, 0x18, 0x82, 0x4e]),
        ("rev64 v0.16b, v1.16b", vec![0x20, 0x08, 0x20, 0x4e]),
        ("rev64 v0.2s, v1.2s", vec![0x20, 0x08, 0xa0, 0x0e]),
        ("extr x0, x1, x2, #8", vec![0x20, 0x20, 0xc2, 0x93]),
        ("movi v0.16b, #0", vec![0x00, 0xe4, 0x00, 0x4f]),
        ("movi v0.8b, #0", vec![0x00, 0xe4, 0x00, 0x0f]),
        ("movi v0.8b, #255", vec![0xe0, 0xe7, 0x07, 0x0f]),
        ("movi v1.16b, #255", vec![0xe1, 0xe7, 0x07, 0x4f]),
        ("movi v1.8b, #1", vec![0x21, 0xe4, 0x00, 0x0f]),
        ("movi v0.2d, #0000000000000000", vec![0x00, 0xe4, 0x00, 0x6f]),
        ("movi v0.2d, #0xffffffffffffffff", vec![0xe0, 0xe7, 0x07, 0x6f]),
        ("movi v2.2d, #0xffffffffffffffff", vec![0xe2, 0xe7, 0x07, 0x6f]),
        ("movi v0.2s, #1", vec![0x20, 0x04, 0x00, 0x0f]),
        ("movi v0.2s, #2", vec![0x40, 0x04, 0x00, 0x0f]),
        ("movi d0, #0000000000000000", vec![0x00, 0xe4, 0x00, 0x2f]),
        ("movi d0, #0xffffffffffffffff", vec![0xe0, 0xe7, 0x07, 0x2f]),
        ("fnmul d0, d1, d2", vec![0x20, 0x88, 0x62, 0x1e]),
        (
            "ld3 { v0.16b, v1.16b, v2.16b }, [x3]",
            vec![0x60, 0x40, 0x40, 0x4c],
        ),
        (
            "ld3r { v0.16b, v1.16b, v2.16b }, [x3]",
            vec![0x60, 0xe0, 0x40, 0x4d],
        ),
        (
            "ld4 { v0.16b, v1.16b, v2.16b, v3.16b }, [x4]",
            vec![0x80, 0x00, 0x40, 0x4c],
        ),
        (
            "ld4r { v0.16b, v1.16b, v2.16b, v3.16b }, [x4]",
            vec![0x80, 0xe0, 0x60, 0x4d],
        ),
    ];

    for (name, bytes) in cases {
        assert_complete_semantics(name, &bytes);
    }
}
