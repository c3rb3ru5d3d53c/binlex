use super::super::support::assert_complete_semantics;

#[test]
fn memory_semantics_regressions_stay_complete() {
    let cases = [
        ("stp x29, x30, [sp, #-16]!", vec![0xfd, 0x7b, 0xbf, 0xa9]),
        ("ldp x29, x30, [sp], #16", vec![0xfd, 0x7b, 0xc1, 0xa8]),
        ("ldrb w0, [x1]", vec![0x20, 0x00, 0x40, 0x39]),
        ("strb w0, [x1]", vec![0x20, 0x00, 0x00, 0x39]),
        ("ldrh w0, [x1]", vec![0x20, 0x00, 0x40, 0x79]),
        ("ldrsw x0, [x1]", vec![0x20, 0x00, 0x80, 0xb9]),
        ("ldursw x0, [x1, #8]", vec![0x20, 0x80, 0x80, 0xb8]),
        ("ldur x0, [x1, #8]", vec![0x20, 0x80, 0x40, 0xf8]),
        ("stur x0, [x1, #8]", vec![0x20, 0x80, 0x00, 0xf8]),
        ("strh w0, [x1]", vec![0x20, 0x00, 0x00, 0x79]),
        ("ldrsb x0, [x1]", vec![0x20, 0x00, 0x80, 0x39]),
        ("ldrsh x0, [x1]", vec![0x20, 0x00, 0x80, 0x79]),
        ("ldursh x0, [x1, #8]", vec![0x20, 0x80, 0x80, 0x78]),
        ("ldurb w0, [x1, #8]", vec![0x20, 0x80, 0x40, 0x38]),
        ("sturb w0, [x1, #8]", vec![0x20, 0x80, 0x00, 0x38]),
        ("sturh w0, [x1, #8]", vec![0x20, 0x80, 0x00, 0x78]),
        ("ldurh w0, [x1, #8]", vec![0x20, 0x80, 0x40, 0x78]),
        ("sttr x0, [x1, #8]", vec![0x20, 0x88, 0x00, 0xf8]),
        ("sttrb w0, [x1, #8]", vec![0x20, 0x88, 0x00, 0x38]),
        ("sttrh w0, [x1, #8]", vec![0x20, 0x88, 0x00, 0x78]),
        ("ldnp x0, x1, [x2]", vec![0x40, 0x04, 0x40, 0xa8]),
        ("ldtr x0, [x1, #8]", vec![0x20, 0x88, 0x40, 0xf8]),
        ("ldtrb w0, [x1, #8]", vec![0x20, 0x88, 0x40, 0x38]),
        ("ldtrh w0, [x1, #8]", vec![0x20, 0x88, 0x40, 0x78]),
        ("ldtrsb x0, [x1, #8]", vec![0x20, 0x88, 0x80, 0x38]),
        ("ldtrsh x0, [x1, #8]", vec![0x20, 0x88, 0x80, 0x78]),
        ("ldtrsw x0, [x1, #8]", vec![0x20, 0x88, 0x80, 0xb8]),
    ];

    for (name, bytes) in cases {
        assert_complete_semantics(name, &bytes);
    }
}
