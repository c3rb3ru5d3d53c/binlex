use super::common::assert_complete_semantics;

#[test]
fn object_semantics_regressions_stay_complete() {
    let cases = [
        ("ldtoken", vec![0xd0, 0x01, 0x00, 0x00, 0x01]),
        ("ldftn", vec![0xfe, 0x06, 0x01, 0x00, 0x00, 0x06]),
        ("newarr", vec![0x8d, 0x01, 0x00, 0x00, 0x01]),
        ("newobj", vec![0x73, 0x01, 0x00, 0x00, 0x0a]),
        ("box", vec![0x8c, 0x01, 0x00, 0x00, 0x01]),
        ("unbox.any", vec![0xa5, 0x01, 0x00, 0x00, 0x01]),
        ("castclass", vec![0x74, 0x01, 0x00, 0x00, 0x01]),
        ("unbox", vec![0x79, 0x01, 0x00, 0x00, 0x01]),
        ("isinst", vec![0x75, 0x01, 0x00, 0x00, 0x01]),
        ("ldvirtftn", vec![0xfe, 0x07, 0x01, 0x00, 0x00, 0x0a]),
        ("mkrefany", vec![0xc6, 0x01, 0x00, 0x00, 0x01]),
        ("refanytype", vec![0xfe, 0x1d]),
        ("refanyval", vec![0xc2, 0x01, 0x00, 0x00, 0x01]),
        ("sizeof", vec![0xfe, 0x1c, 0x01, 0x00, 0x00, 0x01]),
    ];

    for (name, bytes) in cases {
        assert_complete_semantics(name, &bytes);
    }
}
